#!/usr/bin/env python3
"""Run all 12 test cases and generate EVAL_REPORT.md."""

from __future__ import annotations

import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from app.agents.orchestrator import process_claim
from app.models.schemas import ClaimCategory, ClaimHistoryEntry, ClaimSubmission, DocumentInput

DATA_DIR = Path(__file__).resolve().parents[2] / "data"
REPORT_PATH = Path(__file__).resolve().parents[2] / "EVAL_REPORT.md"


def build_claim(tc_input: dict) -> ClaimSubmission:
    docs = [DocumentInput(**d) for d in tc_input["documents"]]
    history = [ClaimHistoryEntry(**h) for h in tc_input.get("claims_history", [])]
    return ClaimSubmission(
        member_id=tc_input["member_id"],
        claim_category=ClaimCategory(tc_input["claim_category"]),
        treatment_date=tc_input["treatment_date"],
        claimed_amount=tc_input["claimed_amount"],
        documents=docs,
        ytd_claims_amount=tc_input.get("ytd_claims_amount", 0),
        claims_history=history,
        simulate_component_failure=tc_input.get("simulate_component_failure", False),
        hospital_name=tc_input.get("hospital_name"),
    )


def evaluate_match(tc: dict, result) -> tuple[bool, str]:
    expected = tc["expected"]
    notes: list[str] = []

    if expected.get("decision") is None:
        if not (result.blocked or result.decision is None):
            return False, "Expected blocked/no decision but got a decision"
        notes.append("Correctly blocked before decision")
    else:
        if result.decision is None:
            return False, f"Expected {expected['decision']} but claim was blocked"
        if result.decision.value != expected["decision"]:
            return False, f"Expected {expected['decision']} got {result.decision.value}"

    if "approved_amount" in expected and result.approved_amount != expected["approved_amount"]:
        return False, f"Expected amount {expected['approved_amount']} got {result.approved_amount}"

    if "rejection_reasons" in expected:
        for r in expected["rejection_reasons"]:
            if r not in result.rejection_reasons:
                return False, f"Missing rejection reason {r}"

    if expected.get("confidence_score") == "above 0.85" and result.confidence_score < 0.85:
        return False, f"Confidence {result.confidence_score} below 0.85"

    if expected.get("confidence_score") == "above 0.90" and result.confidence_score < 0.90:
        return False, f"Confidence {result.confidence_score} below 0.90"

    for req in expected.get("system_must", []):
        combined = (result.member_message + result.reason + str(result.trace)).lower()
        if "same-day" in req.lower() and "same-day" not in combined and "same day" not in combined:
            notes.append(f"Check manually: {req}")
        elif "pre-auth" in req.lower() or "pre-authorization" in req.lower():
            if "pre-auth" not in combined and "pre-authorization" not in combined:
                notes.append(f"Check manually: {req}")
        elif "manual review" in req.lower() and not result.manual_review_recommended and result.decision and result.decision.value != "MANUAL_REVIEW":
            if "manual review" not in combined:
                notes.append(f"Check manually: {req}")

    return True, "; ".join(notes) if notes else "All checks passed"


def format_trace(trace) -> str:
    lines = []
    for step in trace:
        lines.append(f"- **{step.component}** / {step.action} → `{step.status.value}`: {step.message}")
    return "\n".join(lines)


def main() -> None:
    with open(DATA_DIR / "test_cases.json") as f:
        test_cases = json.load(f)["test_cases"]

    passed = 0
    sections: list[str] = [
        "# Evaluation Report — Plum Claims Processing System\n",
        f"Generated by running all {len(test_cases)} test cases through the pipeline.\n",
    ]

    for tc in test_cases:
        claim = build_claim(tc["input"])
        response = process_claim(claim)
        result = response.result
        match, note = evaluate_match(tc, result)
        if match:
            passed += 1

        sections.append(f"## {tc['case_id']}: {tc['case_name']}\n")
        sections.append(f"**Description:** {tc['description']}\n")
        sections.append(f"**Match:** {'✅ PASS' if match else '❌ FAIL'} — {note}\n")
        sections.append(f"**Decision:** `{result.decision.value if result.decision else 'BLOCKED'}`\n")
        if result.blocked:
            sections.append(f"**Block Reason:** {result.block_reason}\n")
            sections.append(f"**Member Message:** {result.member_message}\n")
        sections.append(f"**Approved Amount:** ₹{result.approved_amount:,.2f}\n")
        sections.append(f"**Confidence:** {result.confidence_score}\n")
        if result.rejection_reasons:
            sections.append(f"**Rejection Reasons:** {', '.join(result.rejection_reasons)}\n")
        if result.component_failures:
            sections.append(f"**Component Failures:** {result.component_failures}\n")
        if result.manual_review_recommended:
            sections.append("**Manual Review Recommended:** Yes\n")
        if result.financial_breakdown:
            sections.append(f"**Financial Breakdown:** `{json.dumps(result.financial_breakdown)}`\n")
        if result.line_item_decisions:
            sections.append("**Line Items:**\n")
            for li in result.line_item_decisions:
                status = "Approved" if li.approved else "Rejected"
                sections.append(f"- {li.description}: ₹{li.amount:,.0f} — {status} — {li.reason}\n")
        sections.append(f"**Reason:** {result.reason or result.member_message}\n")
        sections.append("**Trace:**\n")
        sections.append(format_trace(result.trace) + "\n")
        sections.append("---\n")

    sections.insert(2, f"\n**Summary:** {passed}/{len(test_cases)} test cases passed.\n")

    REPORT_PATH.write_text("\n".join(sections), encoding="utf-8")
    print(f"Report written to {REPORT_PATH}")
    print(f"Passed: {passed}/{len(test_cases)}")


if __name__ == "__main__":
    main()
